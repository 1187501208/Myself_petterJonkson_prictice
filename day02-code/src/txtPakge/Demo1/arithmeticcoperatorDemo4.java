package txtPakge.Demo1;

public class arithmeticcoperatorDemo4 {
    //关系运算
    public static void main(String[] args) {
        //1.判断两边是否相等==
        int a=10;
        int b=10;
        int c=20;
        System.out.println(a==b);
        System.out.println(a==c);
        //2.判断两边是否不等!=
        System.out.println(a!=b);
        //1.判断两边大小>=/<=
        System.out.println(a>=c);
        System.out.println(a<=c);
    }
}
